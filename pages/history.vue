      <p>
        As early as 1682, William Penn deeded land in Pennsylvania from the Indians.
        In 1768, the last and most important purchase was made. It was called “New Purchase”
        and connected the northeast and southwest corners of the Commonwealth.
      </p>
      <p>
        After the French and Indian War, land along the Susquehanna River was made
        available to officers of Pennsylvania’s First and Second Regiments. Applications for
        this land were made April 3, 1769; but since it was possible that several officers might
        select the same land, all applications were put into a large box or trunk. From there,
        one by one, each was drawn and through this lottery the land was divided.
      </p>
      <p>
        Most important to us are: Colonel Turbutt Francis, who chose land around the
        future site of Milton; Ensign Augustus Stein, whose land was at the mouth of the Muddy
        River; and Captain Samuel Hunter, who selected his land at the mouth of Warrior Run.
      </p>
      <p>
        In 1774, Reuben Haines, a wealthy brewer from Philadelphia, was buying land
        from the “New Purchase”, one tract, 246 acres from Ensign Stein, was known as
        “Brewers Hall”. Haines also purchased eight more tracts along Muddy Run, including a
        tract called “Rodrigo” which is now known as “Focus Farm” and the subject of this
        paper.
      </p>
      <blockquote class="blockquote">
        <p>
          “Whereas by virtue and in pursuance of an Order on Application
          No. 2209 entered the third day of April, 1769 by Robert Roberts, there hath
          been surveyed a certain tract of land containing two hundred and seventy
          eight acres, and the allowance of six percent for roads, situate on Muddy
          Run, in the county of Northumberland. And whereas the said Robert Roberts
          by his deed dated the 28 th of March 1772 conveyed the said tract of land to
          Joseph Jacobs Wallis who by deed dated the eighth of January 1773 conveys
          the said tract of land to Reuben Haines. And the said Reuben Haines hath
          paid purchase money at the rate of five pounds sterling, per hundred
          acres….” This patent was dated June 13, 1782.
        </p>
        <p>
          Rordigo was described in the deeds as “a tract of land situate on Muddy
          Run, formerly in Berks now Northumberland County. Beginning at a corner
          pine of the Officers Land thence by the same South fifteen degrees West
          224 pr. to a marked White Oak thence South three degrees West 38 pr. to a
          corner White Oak thence by Vacant Land North eighty degrees East 233 pr.
          to a corner White Oak thence North sixteen degrees East 100 pr. to a corner
          White oak of Hugh Roberts Jun. Land thence by the same North thirty four
          degrees West 58 Pr. to a corner Hickory thence by Pine Barrons South fifty
          degrees West 170 pr. to a corner Hickory thence by Joseph Jones’s Land
          South forty one degrees 52 pr. to the first mentioned pine tree and Place of
          Beginning. Containing 278 acres and allowance of six percent to road use.”
        </p>
      </blockquote>
      <p>This acreage actually has 9 sides to it, which is why it sounds so confusing.</p>
      <p>
        Since Reuben Haines remained in Philadelphia, he had his son, Josiah, act as
        his representative and legal agent in Northumberland County. For this work, Josiah
        inherited “Rodrigo” in 1802 at the time of his father’s death. After Josiah died, John
        Smith Haines, his son, in turn inherited “Rodrigo.” It appears these men rented the
        lands; and when John Haines died in 1819 his will shows the 278 acres being divided.
        Half interest was bequeathed to “William Lloyd after he marries Sarah,” John’s sister;
        and the other half was given to Sarah Rittenhouse, John’s cousin.
      </p>
      <p>
        Records indicate that in 1821, William Lloyd and wife, of Northumberland, sold
        127 acres – 126 pr. to William Marr of Turbot Township. William Marr already owned
        much of the land along the Susquehanna River, north of Milton. When he died in 1824,
        his land was left to his widow and nine children, several of whom were minor
        dependents. It appears that in 1834 (ten years later), the oldest son, Joseph, petitioned
        the courts to value the land and throughout the next year there is record of the lands
        being divided with David Marr, fourth son, receiving 2 tracts. “Rodrigo” is shown as
        Tract B on future deeds referring to this division. David was awarded this land, valued
        at $22.23 per acre. Tract B is now considered to be Tract 1 of Focus Farm.
      </p>
      <p>
        The Marr family owned Tract B (or Tract 1) from 1821 until 1883. The land
        passed from William to David as shown above; when David died without a will, the land
        went to Harriet, his widow, and Mary, his daughter. In 1866 Harriet bought her
        daughter’s interest in the land and continued to hold ownership until she sold it to John
        Haus in April 1883. According to the assessment records at the courthouse in Sunbury,
        a house was built on this property in 1865, coincidentally the last year of the Civil War.
      </p>
      <p>
        While Tract 1 of “Focus Farm” appears to have only a few owners and stayed
        pretty much intact, the lower tract beginning with Sarah Rittenhouse’s ownership
        changed both owners and acreage much more often. Sarah Rittenhouse received her
        share of “Rodrigo” in 1819. Her heirs sold the property in 1838 to George Hartman
        whose heirs sold it the next year (1839) to Nicholas Groover, who again sold it the very
        next year (1840) to Nathanial Britain.
      </p>
      <p>
        In 1841, Nathanial Britain sold twelve- and one-half acres to N. B. McCrea, and
        in 1852 sold the remaining acreage to David Kramm. In that same year, Kramm bought
        twelve- and one-half acres from McCrea. The following year, Kramm then sold seventy-
        three acres to Anna Maria Heinen.
      </p>
      <p>
        Records reveal that in 1864, William Heinen, husband of Anna Maria sold eighty-
        four and one half as well as twelve- and one-half acres to David Eshbach. Eshbach in
        turn sold the ninety-nine plus acres to James McCormick in 1867.
      </p>
      <p>
        Not until April and May 1893 were the two tracts of “Focus Farm” again owned by
        a single owner. John Haas purchased all the land known as Tract B from Harriet Marr,
        where it became known as Tract 1 of his farm. He also purchased twenty-two plus
        acres from James McCormick of Sunbury, now considered Tract 2 of this farm. All
        records indicate that John Haas resided in Sunbury when he purchased his farm and
        that he was living there at the time of his death. Therefore, we assume he rented the
        farm to tenant farmers throughout part or all of the thirty-five years he held ownership.
      </p>
      <p>
        Records, again in the Sunbury Courthouse, indicate an agreement drawn
        between the heirs of John Haas and J. D. Hummel as early as September 1913 for the
        purchase of the farm. This arrangement shows a sale price of $7,000 agreed upon at
        that time; however, the actual sale was not filed and confirmed until October 12, 1918.
        The deed contains, “the said two tracts contain altogether 166 acres and 15 pr. more or
        less, and have erected thereon a farmhouse, barn, and other outbuildings.”
      </p>
      <p>
        From the oral history of Florence Hummel Johnson, youngest daughter of J. D.
        and his wife, the family moved to the Turbot Township farm from Union County when
        she was a young girl, perhaps about 6 or 7 years old. She remembered driving the
        cattle across the Susquehanna River, which would allow us to believe they lived in rent
        for several year before buying the property. She said they moved there around 1905,
        but this has not been verified.
      </p>
      <p>
        It does seem that the Hummels were the first owners to reside and farm what has
        become “Focus Farm”. Mr. Hummel owned and farmed the property for 27 years,
        raised his children and one grandson there, and only moved from the farm about a year
        before his death in July 1927. His family continued ownership, but used tenant farmers
        until 1945, when his heirs sold the property to Dr. D. B. Klinetob for $3,000. He was
        purchasing the farm as a place to keep his horses with the intent his son and daughter-
        in-law, Richard and Mary would live there.
      </p>
      <p>
        Mary was the granddaughter of J.D. Hummel and his wife, Maizie. She had
        visited the farm throughout her childhood and had many fond memories of that place.
        Richard had just completed his military service in the Army Air Corps and was returning
        to Milton to begin his practice of Optometry.
      </p>
      <p>
        Finally, May 29, 1948, Richard and Mary bought the farm and were able to take
        up residence there, with the first two of their seven children. They raised their children
        and continues to live on the farm until Mary passed away in 2013. Although the farm
        and buildings have undergone notable changes, the most significant would have come
        in 1968 when the Pennsylvania Department of Highways assumed twenty eight acres to
        construct a by-pass road, thus dividing the farm by taking land from both Tracts 1 and 2.
        The original house, constructed in 1865 and improved upon by the Klinetobs had to be
        moved in order to save it from demolition during this time.
      </p>
      <p>
        Although this may not be an absolute history of the farm, records were accessed
        from the Sunbury Courthouse, the Northumberland County Historical Society, the State
        Library at Harrisburg, The Free Library of Philadelphia, and Milton’s Public Library, as
        well as memories of several family members. In addition, several reference books were
        used, although this history can only be as accurate as the information secured.
      </p>
